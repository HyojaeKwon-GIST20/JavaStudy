package chapter9Example;

public class Granduer extends Car {

	@Override
	public void start() {
		System.out.println("Granduer 시동을 켭니다.");
	}

	@Override
	public void drive() {
		System.out.println("Granduer 달립니다.");

	}

	@Override
	public void stop() {
		System.out.println("Granduer 멈춥니다.");

	}

	@Override
	public void turnOff() {
		System.out.println("Granduer 시동을 끕니다.");

	}

}
